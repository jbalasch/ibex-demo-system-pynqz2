void shiftRowsAES(maskState * mval)
{
	mask tmp; initMasks(&tmp, &(mval->st[0].L[0]), &(mval->st[0].L2[0]));
	
	/* Second row */
	copyMasks(&tmp, &(mval->st[1]));
	copyMasks(&(mval->st[1]), &(mval->st[5]));
	copyMasks(&(mval->st[5]), &(mval->st[9]));
	copyMasks(&(mval->st[9]), &(mval->st[13]));
	copyMasks(&(mval->st[13]), &tmp);

	/* Third row */
	copyMasks(&tmp, &(mval->st[2]));
	copyMasks(&(mval->st[2]), &(mval->st[10]));
	copyMasks(&(mval->st[10]), &tmp);
	copyMasks(&tmp, &(mval->st[6]));
	copyMasks(&(mval->st[6]), &(mval->st[14]));
	copyMasks(&(mval->st[14]), &tmp);
	
	/* Fourth row */
	copyMasks(&tmp, &(mval->st[3]));
	copyMasks(&(mval->st[3]), &(mval->st[15]));
	copyMasks(&(mval->st[15]), &(mval->st[11]));
	copyMasks(&(mval->st[11]), &(mval->st[7]));
	copyMasks(&(mval->st[7]), &tmp);
}

/* Should mxtime be updated every time? */
void mixColumnsAES(mask * st0, mask * st1, mask * st2, mask * st3)
{
	mask t, u, v;
	
	/* Compute values t, u, v */
	copyMasks(&t, st0);
	maskAddition(&t, &t, st1);
	maskAddition(&t, &t, st2);
	maskAddition(&t, &t, st3);
	
	copyMasks(&u, st0);
	
	/* 1st byte */
	copyMasks(&v, st0);
	maskAddition(&v, &v, st1);
	maskMultConst(&v, &v, 0x02);
	maskAddition(st0, st0, &v);
	maskAddition(st0, st0, &t);
	
	/* 2nd byte */
	copyMasks(&v, st1);
	maskAddition(&v, &v, st2);
	maskMultConst(&v, &v, 0x02);
	maskAddition(st1, st1, &v);
	maskAddition(st1, st1, &t);
	
	/* 3rd byte */
	copyMasks(&v, st2);
	maskAddition(&v, &v, st3);
	maskMultConst(&v, &v, 0x02);
	maskAddition(st2, st2, &v);
	maskAddition(st2, st2, &t);
	
	/* 4th byte */
	copyMasks(&v, st3);
	maskAddition(&v, &v, &u);
	maskMultConst(&v, &v, 0x02);
	maskAddition(st3, st3, &v);
	maskAddition(st3, st3, &t);
}

void nextRoundKeyAES(maskState * mval, uint8_t rcon)
{
	mask tmp; initMasks(&tmp, &(mval->st[0].L[0]), &(mval->st[0].L[0]));

	/* First Column */
	copyMasks(&tmp, &(mval->st[12]));
	sboxAES(&tmp);
	maskAddition(&(mval->st[3]), &tmp, &(mval->st[3]));
	
	copyMasks(&tmp, &(mval->st[13]));
	sboxAES(&tmp);
	maskAddition(&(mval->st[0]), &tmp, &(mval->st[0]));
	maskAddConst(&(mval->st[0]), &(mval->st[0]), rcon);
	
	copyMasks(&tmp, &(mval->st[14]));
	sboxAES(&tmp);
	maskAddition(&(mval->st[1]), &tmp, &(mval->st[1]));
	
	copyMasks(&tmp, &(mval->st[15]));
	sboxAES(&tmp);
	maskAddition(&(mval->st[2]), &tmp, &(mval->st[2]));
	
	/* Second Column */
	maskAddition(&(mval->st[4]), &(mval->st[4]), &(mval->st[0]));
	maskAddition(&(mval->st[5]), &(mval->st[5]), &(mval->st[1]));
	maskAddition(&(mval->st[6]), &(mval->st[6]), &(mval->st[2]));
	maskAddition(&(mval->st[7]), &(mval->st[7]), &(mval->st[3]));
	
	/* Third Column */
	maskAddition(&(mval->st[8]), &(mval->st[8]), &(mval->st[4]));
	maskAddition(&(mval->st[9]), &(mval->st[9]), &(mval->st[5]));
	maskAddition(&(mval->st[10]), &(mval->st[10]), &(mval->st[6]));
	maskAddition(&(mval->st[11]), &(mval->st[11]), &(mval->st[7]));
	
	/* Fourth Column */
	maskAddition(&(mval->st[12]), &(mval->st[12]), &(mval->st[8]));
	maskAddition(&(mval->st[13]), &(mval->st[13]), &(mval->st[9]));
	maskAddition(&(mval->st[14]), &(mval->st[14]), &(mval->st[10]));
	maskAddition(&(mval->st[15]), &(mval->st[15]), &(mval->st[11]));

}
